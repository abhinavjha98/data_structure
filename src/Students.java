import java.util.*;
public class Students {
    int Roll_no;
    String name;
    double marks;
    public Students(int Roll_no,String name,double marks){
        this.Roll_no=Roll_no;
        this.name=name;
        this.marks=marks;
    }
    public String toString(){
        return "Student [rollNo=" + Roll_no + ", name=" + name + ", marks=" + marks + "]";
    }
}

    
    
